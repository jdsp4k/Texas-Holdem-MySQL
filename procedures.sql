CREATE PROCEDURE CLEAN ()
BEGIN

    DELETE FROM ROUND;
    DELETE FROM _MATCH;

    UPDATE SEAT
    SET SitterUsername=NULL;

    UPDATE _TABLE
    SET TableState=0;

    ALTER TABLE _MATCH
    AUTO_INCREMENT = 1;

    ALTER TABLE ROUND
    AUTO_INCREMENT = 1;

END; //

